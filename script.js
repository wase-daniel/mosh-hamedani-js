function switchStatements(num) {
  var answer = "";
  switch (num) {
    case 1:
      answer = "number 1";
      break;
    case 2:
      answer = "number 2";
      break;
    case 3:
      answer = "number 3";
      break;
    case 4:
      answer = "number 4";
      break;
  }
  return answer;
}

console.log(switchStatements(1));
